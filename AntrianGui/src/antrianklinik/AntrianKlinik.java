    private void btnPanggilUmumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPanggilUmumActionPerformed
         String text = textTampilan.getText();
    if (text.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Antrian kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String[] lines = text.split("\n");
    StringBuilder newText = new StringBuilder();
    boolean found = false;
    for (String line : lines) {
        if (!found && line.contains("A-")) {
            textUmum.setText(line);
            found = true;
        } else {
            newText.append(line).append("\n");
        }
    }

    if (!found) {
        JOptionPane.showMessageDialog(this, "Tidak ada antrian umum", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }

    textTampilan.setText(newText.toString());
    }//GEN-LAST:event_btnPanggilUmumActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
      String nama = textNama.getText();
        String nik = textNIK.getText();
        String nomorAntrian = "";

        // Validasi untuk memastikan textNama dan textNIK tidak kosong
        if (nama.isEmpty() || nik.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan NIK harus diisi", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi untuk memastikan textNIK berbentuk angka
        try {
            Long.parseLong(nik);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "NIK harus berupa angka", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi untuk memastikan salah satu radio button dipilih
        if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) {
            JOptionPane.showMessageDialog(this, "Pilih jenis layanan", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (jRadioButton1.isSelected()) {
            nomorAntrian = "A-" + counterUmum;
            antrianUmum.add(new Patient<>(nama, nik, nomorAntrian, "Umum"));
            counterUmum++;
        } else if (jRadioButton2.isSelected()) {
            nomorAntrian = "C-" + counterGigi;
            antrianGigi.add(new Patient<>(nama, nik, nomorAntrian, "Gigi"));
            counterGigi++;
        }

        textTampilan.append("Nama: " + nama + ", NIK: " + nik + ", Nomor Antrian: " + nomorAntrian + "\n");
        simpanData(nama, nik, nomorAntrian);

        jumlahPasien++; // Menambah jumlah pasien

        // Menampilkan jumlah pasien pada textJumlah
        textJumlah.setText(String.valueOf(jumlahPasien));

        textNama.setText("");
        textNIK.setText("");
        buttonGroup1.clearSelection();
    
    }//GEN-LAST:event_btnTambahActionPerformed

