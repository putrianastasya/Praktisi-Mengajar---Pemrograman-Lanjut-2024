     private void simpanData(String nama, String nik, String nomorAntrian) {
           try (BufferedWriter writer = new BufferedWriter(new FileWriter("daftarAntrian.txt", true))) {
            writer.write("Nama: " + nama + ", NIK: " + nik + ", Nomor Antrian: " + nomorAntrian);
            writer.newLine();
        } catch (IOException e) {
            System.err.println("Gagal menyimpan data: " + e.getMessage());
        }
    }
     
     
